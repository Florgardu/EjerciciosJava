package clases;

public class PadrePrueba {
	
	private int ojos=2;
	
	private int colorPelo;
	

	public PadrePrueba( int colorPelo) {
		this.colorPelo = colorPelo;
	}


	public int getOjos() {
		return ojos;
	}


	public int getColorPelo() {
		return colorPelo;
	}


	public void setOjos(int ojos) {
		this.ojos = ojos;
	}


	public void setColorPelo(int colorPelo) {
		this.colorPelo = colorPelo;
	}

	
	
	
	
}
